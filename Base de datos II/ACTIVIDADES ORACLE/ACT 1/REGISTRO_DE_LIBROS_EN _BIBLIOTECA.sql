/*Crear una tabla llamada libros */
CREATE TABLE LIBROS(
	ID_LIBRO NUMBER PRIMARY KEY ,
	TITULO VARCHAR2(130)NOT NULL,
	AUTOR VARCHAR2(130) NOT NULL, 
	FECHA_PUBLICACION DATE NOT NULL,
	DISPONIBLE NUMBER(1) NOT NULL
);

